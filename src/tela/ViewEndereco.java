    private void bntEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEnviarActionPerformed
        // TODO add your handling code here:
        Endereco c = new Endereco();
        EnderecoDAO dao = new EnderecoDAO();
        Cliente cc = new Cliente();

        c.setLogadouro(txtLogadouro.getText());
        c.setNumero(Integer.parseInt(txtNumero.getText()));
        c.setCidade(txtCidade.getText());
        c.setEstado(txtEstado.getText());
        cc.setId(Integer.parseInt(txtIdCliente.getText()));
        c.setIdcliente(cc);

        dao.create(c);

        txtLogadouro.setText("");
        txtNumero.setText("");
        txtCidade.setText("");
        txtEstado.setText("");
        txtIdCliente.setText("");

        readJTABLE();
    }//GEN-LAST:event_bntEnviarActionPerformed

    private void bntExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntExcluirActionPerformed
        // TODO add your handling code here:
        if(jTTable.getSelectedRow() != -1){

            Endereco c = new Endereco();
            EnderecoDAO dao = new EnderecoDAO();

            c.setIdendereco((int)jTTable.getValueAt(jTTable.getSelectedRow(), 0));

            dao.delete(c);

            txtLogadouro.setText("");
            txtNumero.setText("");
            txtCidade.setText("");
            txtEstado.setText("");
            txtIdCliente.setText("");

            readJTABLE();

        }else{
            JOptionPane.showMessageDialog(null, "Selecione um Endereço para excluir !");
        }
    }//GEN-LAST:event_bntExcluirActionPerformed

    private void bntAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAlterarActionPerformed
        // TODO add your handling code here:
        if(jTTable.getSelectedRow() != - 1){
            Endereco c = new Endereco();
            EnderecoDAO dao = new EnderecoDAO();
            Cliente cc = new Cliente();

            c.setIdendereco((int)jTTable.getValueAt(jTTable.getSelectedRow(), 0));

            c.setLogadouro(txtLogadouro.getText());
            c.setNumero(Integer.parseInt(txtNumero.getText()));
            c.setCidade(txtCidade.getText());
            c.setEstado(txtEstado.getText());

            cc.setId(Integer.parseInt(txtIdCliente.getText()));
            c.setIdcliente(cc);

            dao.update(c);

            txtIdCliente.setText("");
            txtLogadouro.setText("");
            txtNumero.setText("");
            txtCidade.setText("");
            txtEstado.setText("");

            readJTABLE();

        }
    }//GEN-LAST:event_bntAlterarActionPerformed

