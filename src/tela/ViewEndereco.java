     public void readJTABLE(){
        DefaultTableModel modelo = (DefaultTableModel) jTTable.getModel();
        modelo.setNumRows(0);
        EnderecoDAO cdao = new EnderecoDAO();
       
        
        for(Endereco c : cdao.read()){
            modelo.addRow(new Object[]{
                c.getIdendereco(),
                c.getLogadouro(),
                c.getNumero(),
                c.getCidade(),
                c.getEstado(),
                c.getIdcliente().getId(),
                c.getId(),
                c.getNome(),
                c.getEmail()
            });  
        } 
    }
    
    public void readJTABLEForDesc(Integer desc){
        DefaultTableModel modelo = (DefaultTableModel) jTTable.getModel();
        modelo.setNumRows(0);
        EnderecoDAO cdao = new EnderecoDAO();
        
        for(Endereco c : cdao.readForDesc(desc)){
            modelo.addRow(new Object[]{
                c.getIdendereco(),
                c.getLogadouro(),
                c.getNumero(),
                c.getCidade(),
                c.getEstado(),
                c.getIdcliente().getId(),
                c.getId(),
                c.getNome(),
                c.getEmail()
            });
        }
    }

